bits 16
start_graphics_mode:
    mov ah,0
    mov al,13h
    int 10h
    ret
draw_pixel :
	; ds , es , fs , gs , cs , si , di , bx 
    mov cx, 0A000h ; The offset to video memory
    mov es,cx
    mov ax,[esp+4] ; Y coord
    mov cx,320     
    mul cx
    mov bx,[esp+6] ; X coord
    add ax,bx
    mov di,ax
    mov dx,[esp+2]
    mov byte [es:di],dl
    ret

draw_rectangle :
	push ax 
	push bx 
	push cx 
	push dx 
	push si 
	push di 
	push ds 
    xor bx,bx
    loop1 :
        xor ax,ax
        loop2:
            mov di , [esp+24] ;x
            mov si , [esp+22] ;y

            add di ,bx
            add si ,ax

            mov dx , [esp+16] ; color
            push dx
            push bx
            push ax
            push di
            push si
            push dx
            call  draw_pixel
            add esp,6
            pop ax
            pop bx
            pop dx

            mov si , [esp+18] ; height
            inc ax
            cmp ax , si
            jl loop2
        mov di , [esp+20] ; width
        inc bx
        cmp bx , di
        jl loop1
	pop ds 
	pop di 
	pop si 
	pop dx 
	pop cx 
	pop bx 
	pop ax 
	
    ret
draw_background:
    push 60
    push 0
    push 2
    push 200
    push 0fh    ; left border
    call draw_rectangle
    add esp , 10

    push 60
    push 0
    push 200
    push 2
    push 0fh   ;  top border
    call draw_rectangle
    add esp , 10

    push 258
    push 0
    push 2      ; right border 
    push 200
    push 0fh
    call draw_rectangle
    add esp , 10

    push 60
    push 198
    push 200
    push 2         ; 
    push 0fh
    call draw_rectangle
    add esp , 10

    push 62
    push 2
    push 196
    push 196
    push 6fh
    call draw_rectangle
    add esp , 10
    ret
stop_graphics_mode:
    mov ax , 03h 
    int 10
	ret 

